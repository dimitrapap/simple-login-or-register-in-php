<h1>TRAVEL TIPS FOR MILOS GREECE</h1>
  <p>Δεύτερη στάση των καλοκαιρινών μου διακοπών η αγαπημένη σε όλους σας -αν κρίνω από τα πόσα μηνύματα έλαβα ότι είναι το νησί που πηγαίνετε συχνά- η Νάξος. Είναι η πρώτη φορά που επισκέφτηκα την Νάξο, το μεγαλύτερο νησί των Κυκλάδων και πραγματικά με κέρδισε τόσο με την φιλοξενία των ανθρώπων όσο και με το όμορφα μέρη που επισκέφτηκα.

Σίγουρα αν επιλέξετε για τις διακοπές σας την Νάξο πρέπει να επισκεφτείτε έστω 1-2 από τους πολλούς αρχαιολογικούς χώρους, αφού το νησί έχει άμεση σχέση με την αρχαία Ελληνική ιστορία. Ένας από τους μύθους είναι ο εξής: Στη Νάξο ο Θησέας εγκατέλειψε την Αριάδνη, η οποία τον είχε βοηθήσει προηγουμένως στην Κρήτη να σκοτώσει το Μινώταυρο με τον όρο να την πάρει μαζί του, την οποία στη συνέχεια πήρε σύζυγό του ο θεός Διόνυσος.

Must visit:

Η Πορτάρα, η τεράστια μαρμάρινη πύλη ενός εκατόμπεδου ναού που ξεκίνησε να χτίζεται τον 6ο π.Χ. αιώνα, πιθανότατα προς τιμήν του θεού Απόλλωνα. * δες παρακάτω φωτογραφία.

Η Νάξος πέρα όμως από την ιστορία της είναι γνωστή και για τα προιόντα που παράγει. Την ξακουστή γραβίερα Ναξου, τις πατάτες, αχ πατάτες Νάξου, και την γούνα (καπνιστό ψάρι που δοκίμασα στο Μεζε- Μεζέ στην χώρα). Must have όμως είναι και η βόλτα στα χωρία της Νάξου, τα δικά μου αγαπημένα ήταν το Χαλκί, ήσυχο και instagram friendly, αλλά και το χωριό Γαλήνη – κυριολεκτικά γαλήνη-. *δες τελευταία φωτογραφία.

Μετά όμως από την εξερεύνηση σίγουρα θα θέλεις να αφιερώσεις μια ημέρα στην θάλλασα και την ξεκούραση όποτε μπορείς να επισκεφτείς κάποιο από τα χαλαρά beach bar στην παραλία Αγιος Προκόπιος που προσωπικά την λάτρεψα και θεωρώ μια από της καλύτερες παραλίες που έχω παει. Αν είσαι cocktail lover σαν εμένα και αγαπάς το ηλιοβασίλεμα σου πρέπει να επισκεφτείς το Τortuga, δεν λέω περισσότερα αρκεί να ρίξεις μια ματια στο Instagram τους.

Ένα είναι σίγουρο, είτε ακολουθήσεις τα δικά μου tips, ή πας με το δικό σου εξερευνητικό ένστικτο, η Νάξος είναι ένα μεγάλο, πανέμορφο νησί, που σε περιμένει να την ανακαλύψεις! Καλές Διακοπές.
