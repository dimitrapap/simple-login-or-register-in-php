<h1>Our trip to Milos Greece</h1>
  <p>Arriving at the port of Adamas which is also the city of Milos it was a 15 minute drive to our hotel Salt which is located in the fisherman village Pollonia located in the north of the island. Salt is a small and private all white boutique hotel with 10 rooms at the end off a cliff from where guests can enjoy dreamy sunset views over the ocean from their room terrace Jacuzzi. Breakfast is served in picknick style in a basket every morning to your room. Just a short walk away from the hotel you find the cute little town of Pollonia with cute restaurants like Almyra and Liofyto offering the best  traditional greek specialities.

Milos, the island of beaches, has only 5000 inhabitants but more than 70 beaches and when I say beaches here I mean the kind of beaches you donâ€™t forget that easily. Get a car and cruise around the island to explore the hidden treasures of the island. My favorite beach was the all white moon landscape like Sarakiniko beach. Walking around Sarakiniko beach almost feels like being on a different planet in some kind of fantasy movie. Chill by the sea cave and watch the water kissing the rocks over and over again.

Another beach I loved is Tsigrado which is a very small beach that requires a little adventure to get to, a wood ladder, and climbing along some hanging ropes being the only access to get to this relaxing place in between cliffs. Firiplaka is another highlight beach, being stretched out long along the coastline and next to volcanic rocks shimmering in red, yellow and brown colors.

For some volcanic food you can make a lunch stop at taverna Sirocco on Paliochori beach where your food is cooked in the heat of the sand.

We also did a catamaran trip to the famous pirate rocks of Kleftiko beach and the lagoons around which are only accessible by boat. You also pass by Mandraika and Klima, two cute little villages with multicolored fisherman houses.

The ancient theatre also overlooking the town of Klima is another attraction when on the island. You can visit the catacombs near the ancient city to learn about human made caves of the Roman period.

I still catch myself daydreaming about the sunset views from the all white village Plaka with its cute alleys that we visited at the end of our time in Milos. I loved Milos for its unique beaches and can recommend the island to all beachlovers, couples and families who love to explore a different beach everyday far away from the crowds.

